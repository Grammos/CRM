     public void tabelaLoad()
    {
        List<Kontrata>lista=kontrataRepository.findAll();
        kontrataTableModel.add(lista);
        tabela.setModel(kontrataTableModel);
        kontrataTableModel.fireTableDataChanged();
        tabelaSelectedIndexChange();
    }
    
    public void tabelaSelectedIndexChange()
    {
        ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting())
                    return;
                ListSelectionModel rowSM = (ListSelectionModel)e.getSource();
                int selectedIndex = rowSM.getMinSelectionIndex();
                if(selectedIndex > -1){
                    Kontrata item = kontrataTableModel.getKontrata(selectedIndex);
                    txt_Emri.setText(item.getEmri());
                    txt_Kompania.setText(item.getKompania());
                    txt_Pozita.setText(item.getPozita());
                    txt_Paga.setText(item.getPaga()+"");
                    data_kalendari.setDate(item.getData()); 
                }
            }
        });
    
    }
    
