    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        try{
            int row = tabela.getSelectedRow();
            if(txt_Emri.getText()!=null && !txt_Emri.getText().equals("")&&txt_Kompania.getText()!=null && !txt_Kompania.getText().equals("") &&  txt_Pozita.getText()!=null && !txt_Pozita.getText().equals("")&& txt_Paga.getText()!=null && !txt_Paga.getText().equals("")&& data_kalendari.getDate() != null && !data_kalendari.getDate().equals("")){
                if(row == -1){
                    Kontrata k=new Kontrata();
                    k.setEmri(txt_Emri.getText());
                    k.setKompania(txt_Kompania.getText());
                    k.setPozita(txt_Pozita.getText());
                    k.setPaga(Integer.parseInt(txt_Paga.getText()));
                    k.setData(data_kalendari.getDate());
                    kontrataRepository.create(k);
                    JOptionPane.showMessageDialog(this, "E dhëna u ruajt me sukses!");
                }
                else{
                    Kontrata k=kontrataTableModel.getKontrata(row);
                    k.setEmri(txt_Emri.getText());
                    k.setKompania(txt_Kompania.getText());
                    k.setPozita(txt_Pozita.getText());
                    k.setPaga(Integer.parseInt(txt_Paga.getText()));
                    k.setData(data_kalendari.getDate());
                    kontrataRepository.edit(k);
                    JOptionPane.showMessageDialog(this, "E dhëna u editua me sukses!");
                }
                emptyObject();
                tabelaLoad();
            }
            else{
                JOptionPane.showMessageDialog(this, "Ju lutem plotësoni të gjitha fushat e detyrushme!");
            }
        }catch(UsersException re){
            JOptionPane.showMessageDialog(this, re.getMessage());

        }
    }//GEN-LAST:event_btn_SaveActionPerformed

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        try{
            int row=tabela.getSelectedRow();
            if(row >-1)
            {   
                Object [] ob={"Po","Jo"};
                int i = javax.swing.JOptionPane.showOptionDialog(this, "A jeni i sigurt që doni ta fshini ?","",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
                if(i==0)
                {   
                    Kontrata k=this.kontrataTableModel.getKontrata(row);
                    kontrataRepository.remove(k);
                    tabelaLoad();
                    emptyObject();
                    JOptionPane.showMessageDialog(this, "E dhëna është fshir me sukses!");
                }
                else{
                JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgjë për të fshir!");
                }
            }
        
        }catch(UsersException ue)
        {
            JOptionPane.showMessageDialog(this, ue.getMessage());
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

